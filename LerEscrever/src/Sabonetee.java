public class Sabonetee {
	
	private String cheiro;
	private String cor;
	
	
	
	
	
	public Sabonetee(String cheiro, String cor) {
		super();
		this.cheiro = cheiro;
		this.cor = cor;

	}





	@Override
	public String toString() {
		return "Sabonetee cheiro=" + cheiro + ", cor=" + cor + " ";
	}




}
