
public class Sabonete {
	
	private String cheiro;
	private String cor;
	
	
	
	
	
	public Sabonete(String cheiro, String cor) {
		super();
		this.cheiro = cheiro;
		this.cor = cor;
	}


	@Override
	public String toString() {
		return " Cheiro do sabonete: " + cheiro + "\n cor do sabonete" + cor + " ";
	}
	

	
	
	
	
	
	
	
	
	
		
}
